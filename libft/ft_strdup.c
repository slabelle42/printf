#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	dst_len;
	char	*dst;
	int		i;

	dst_len = ft_strlen(src) + 1;
	if (!(dst = malloc(sizeof(char) * dst_len)))
		return (NULL);
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
