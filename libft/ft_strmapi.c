#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t			mapi_len;
	char			*mapi;
	unsigned int	i;

	if (!(s && f))
		return (NULL);
	mapi_len = ft_strlen(s);
	if (!(mapi = malloc(sizeof(char) * (mapi_len + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		mapi[i] = f(i, s[i]);
		i++;
	}
	mapi[i] = '\0';
	return (mapi);
}
