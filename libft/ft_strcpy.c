#include "libft.h"

char		*ft_strcpy(char *dst, char *src)
{
	int		len;

	len = 0;
	while (src[len] != '\0')
	{
		dst[len] = src[len];
		len++;
	}
	dst[len] = '\0';
	return (dst);
}
