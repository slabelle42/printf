#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	join_len;
	char	*join;
	int		i;
	int		j;

	join_len = ft_strlen(s1) + ft_strlen(s2) + 1;
	if (!(join = malloc(sizeof(char) * join_len)))
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i])
	{
		join[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		join[i] = s2[j];
		i++;
		j++;
	}
	join[i] = '\0';
	return (join);
}
